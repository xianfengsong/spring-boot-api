class Solution200 {
    int ans =0;
    char m = '-';
    public int numIslands(char[][] grid) {
        for(int i=0;i<grid.length;i++){
            for(int j=0;j<grid[0].length;j++){
                t(grid,i,j);
            }
        }
        return ans;
    }
    public void t(char[][]arr,int i,int j){
        if(i<0||i>=arr.length||j<0||j>=arr[0].length){
            return;
        }
        char c = arr[i][j];
        if(m==c){
            return;
        }
        if('0'==c){
            arr[i][j]=m;
        }else{
            t(arr,i+1,j);
            t(arr,i-1,j);
            t(arr,i,j-1);
            t(arr,i,j+1);
            ans+=1;
        }
    }
}
